/*
    LOOPAR
    ----------------------------------------------------
    
    for-loop - Loopa genom en lista ett givet antal gånger.
    
        for (int i = 0; i<10; i++) {}
    
    foreach-loop - Loopa genom alla objekt i en lista.
        
        string[] names = new string[] { "Hans", "Tommy", "Joakim", "Jeanette" };
        foreach (var name in names) 
        {
            Console.WriteLine(name);
        }
    
    while-loop - loopa om en condition möts.
        

        int number = 0;
        while (number < 10) 
        { 
            Console.WriteLine(number)
            number++;
        }

    do-while-loop - loopa minst en gång och kolla sedan tillståndet.
        
        do
        {
            Console.WriteLine("number")
            number++
        } while (number<1);


*/


